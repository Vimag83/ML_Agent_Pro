def interpretar_distribucion(series, nombre):
    """Genera interpretación automática de una distribución."""
    texto = []
    skew = series.skew()
    kurt = series.kurtosis()
    texto.append(f"La variable <strong>{nombre}</strong> presenta una asimetría de {skew:.2f}")
    if abs(skew) > 1:
        texto.append("indicando una distribución fuertemente sesgada" +
                     (" hacia la derecha (cola larga positiva)" if skew > 0 else " hacia la izquierda"))
    elif abs(skew) > 0.5:
        texto.append("con un sesgo moderado")
    else:
        texto.append("con una distribución aproximadamente simétrica")
    if kurt > 3:
        texto.append(f". La curtosis de {kurt:.2f} sugiere colas pesadas (presencia de valores atípicos)")
    elif kurt < -1:
        texto.append(f". La curtosis de {kurt:.2f} indica una distribución platicúrtica (más plana de lo normal)")
    texto.append(f". Rango: [{series.min():.2f}, {series.max():.2f}], Media: {series.mean():.2f}, Mediana: {series.median():.2f}")
    return " ".join(texto) + "."

def interpretar_correlacion(corr_matrix, umbral=0.7):
    """Detecta y describe correlaciones altas."""
    pares = []
    cols = corr_matrix.columns
    for i in range(len(cols)):
        for j in range(i + 1, len(cols)):
            v = corr_matrix.iloc[i, j]
            if abs(v) >= umbral:
                pares.append((cols[i], cols[j], v))
    if not pares:
        return "No se detectaron correlaciones altas (|r| >= 0.7) entre las variables numéricas. Las variables parecen ser independientes entre sí, lo cual es favorable para los modelos de clasificación."
    texto = f"Se detectaron <strong>{len(pares)} par(es)</strong> con correlación alta (|r| >= 0.7): "
    for a, b, v in pares:
        tipo = "positiva" if v > 0 else "negativa"
        texto += f"<strong>{a}</strong> y <strong>{b}</strong> (r={v:.2f}, {tipo}); "
    texto += "Estas correlaciones pueden indicar redundancia. Considere eliminar una variable de cada par para evitar multicolinealidad."
    return texto

def interpretar_resultados(resultados_df):
    """Interpretación automática de la tabla de resultados."""
    if resultados_df.empty:
        return ""
    mejor = resultados_df.loc[resultados_df["Accuracy CV Mean"].idxmax()]
    peor = resultados_df.loc[resultados_df["Accuracy CV Mean"].idxmin()]
    texto = f"""
    <div class="interpretacion">
    <strong>Interpretación automática de resultados:</strong><br><br>
    El modelo con mejor desempeño es <strong>{mejor['Modelo']}</strong> con un Accuracy medio de
    <strong>{mejor['Accuracy CV Mean']:.4f}</strong> y F1 medio de <strong>{mejor['F1 CV Mean']:.4f}</strong>.<br><br>
    El modelo con menor desempeño es <strong>{peor['Modelo']}</strong> con Accuracy de
    <strong>{peor['Accuracy CV Mean']:.4f}</strong>.<br><br>
    """
    rango = resultados_df["Accuracy CV Mean"].max() - resultados_df["Accuracy CV Mean"].min()
    if rango < 0.03:
        texto += "La diferencia entre modelos es mínima (< 3%), lo que sugiere que todos capturan patrones similares. "
        texto += "En este caso, el modelo más simple (probablemente Decision Tree) podría ser preferido por interpretabilidad."
    elif rango < 0.10:
        texto += f"Existe una diferencia moderada de {rango:.2%} entre el mejor y peor modelo. "
        texto += f"Se recomienda <strong>{mejor['Modelo']}</strong> como candidato principal para optimización."
    else:
        texto += f"La diferencia de {rango:.2%} es significativa, indicando que algunos modelos no logran capturar la estructura de los datos. "
        texto += f"<strong>{mejor['Modelo']}</strong> muestra una clara ventaja y debe ser priorizado."
    # Desbalance entre accuracy y F1
    for _, row in resultados_df.iterrows():
        diff = row["Accuracy CV Mean"] - row["F1 CV Mean"]
        if diff > 0.05:
            texto += f"<br><br>⚠️ <strong>{row['Modelo']}</strong> muestra una brecha Accuracy-F1 de {diff:.2%}, "
            texto += "lo que puede indicar desbalance de clases o predicción sesgada hacia la clase mayoritaria."
    texto += "</div>"
    return texto

