                ax_lime.set_facecolor(C_BG)
                ax_lime.tick_params(colors=C_TEXT, labelsize=9)
                for text in ax_lime.get_yticklabels() + ax_lime.get_xticklabels():
                    text.set_color(C_TEXT)
                ax_lime.set_title("LIME — Explicación Local", color=C_TEXT, fontweight="bold")
                plt.tight_layout()
                st.pyplot(fig_lime)
                plt.close(fig_lime)

                # Predicción real vs predicha
                y_real = st.session_state.label_encoder.inverse_transform([y_test[idx_inst]])[0]
                y_proba = model.predict_proba(X_test_p[idx_inst].reshape(1, -1))
